    public static void Write()
    {
        LocalizePlayerStats();
        LocalizeAchievementTypes();
        LocalizeFactions();
        List<string> lines = new();
        foreach (KeyValuePair<string, string> kvp in keys)
        {
            lines.Add($"{kvp.Key}: '{kvp.Value}'");
        }
        AlmanacPlugin.AlmanacDir.WriteAllLines("Almanac.English.yml", lines);
        if (Configs.AddLogs) AlmanacPlugin.AlmanacLogger.LogDebug("Almanac.Localize.Keys.Write");
    }
    private static void LocalizePlayerStats()
    {
        foreach (PlayerStatType type in Enum.GetValues(typeof(PlayerStatType)))
        {
            _ = new Key($"$playerstat_{type.ToString().ToLower()}", Helpers.SplitCamelCase(type.ToString()));
        }
    }
    private static void LocalizeAchievementTypes()
    {
        foreach (AchievementType type in Enum.GetValues(typeof(AchievementType)))
        {
            _ = new Key($"$label_{type.ToString().ToLower()}", Helpers.SplitCamelCase(type.ToString()));
        }
    }
    private static void LocalizeFactions()
    {
        foreach (Character.Faction type in Enum.GetValues(typeof(Character.Faction)))
        {
            _ = new Key($"$faction_{type.ToString().ToLower()}", Helpers.SplitCamelCase(type.ToString()));
        }
    }
    private class Key
    {
        public readonly string key;
        public Key(string key, string value)
        {
            this.key = key;
            keys[this.key.Replace("$", string.Empty)] = value;
        }
    }
    
